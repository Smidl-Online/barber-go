/**
 * Returns the Czech vocative (5. pád) of a first name if it is
 * recognized as a common Czech name. For non-Czech / unknown names,
 * returns the original form (1. pád) to avoid incorrect declension.
 *
 * @example
 * czechVocative('Jan')    // 'Jane'
 * czechVocative('Tereza') // 'Terezo'
 * czechVocative('John')   // 'John'
 */
export function czechVocative(firstName: string): string {
  if (!firstName) return firstName;

  const normalized = firstName.trim().toLowerCase();
  if (CZECH_NAMES.has(normalized)) {
    return vocative(firstName);
  }

  return firstName;
}
